// CHDPoker hash table lookup.
type CHDPoker struct {
	// Random hash function table.
	r     []uint64
	r_len uint64
	// Array of indices into hash function table r. We assume there aren't
	// more than 2^16 hash functions O_o
	indices [2444]uint16
	i_len   uint64
	// Final table of values.
	//keys   []uint32
	values [4888]uint16
	v_len  uint64
}

type chdHasherPoker struct {
	r       []uint64
	size    uint64
	buckets uint64
	rand    *rand.Rand
}

type bucketPoker struct {
	index  uint64
	keys   []uint32
	values []uint16
}

// Intermediate data structure storing buckets + outer hash index.
type bucketVectorPoker []bucketPoker

func (b bucketVectorPoker) Len() int           { return len(b) }
func (b bucketVectorPoker) Less(i, j int) bool { return len(b[i].keys) > len(b[j].keys) }
func (b bucketVectorPoker) Swap(i, j int)      { b[i], b[j] = b[j], b[i] }

// Build a new CDH MPH.
type CHDBuilderPoker struct {
	keys   []uint32
	values []uint16
}

func hasherPoker(data uint32) uint64 {
	var hash uint64 = 14695981039346656037

	for i := 0; i < 4; i++ {
		hash ^= uint64(data & uint32(0xFF<<(8*i)))
		hash *= 1099511628211
	}

	return hash
}

// Create a new CHD hash table builder.
func BuilderPoker() *CHDBuilderPoker {
	return &CHDBuilderPoker{}
}

// Add a key and value to the hash table.
func (b *CHDBuilderPoker) Add(key uint32, value uint16) {
	b.keys = append(b.keys, key)
	b.values = append(b.values, value)
}

