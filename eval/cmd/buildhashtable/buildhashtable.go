// Try to find a hash function that does not cause collisions with table, when
// applied to the keys in the bucket.
func tryHashPoker(hasher *chdHasherPoker, seen map[uint64]bool, keys []uint32, values *[4888]uint16, indices *[2444]uint16, bucket *bucketPoker, ri uint16, r uint64) bool {
	// Track duplicates within this bucket.
	duplicate := make(map[uint64]bool)
	// Make hashes for each entry in the bucket.
	hashes := make([]uint64, len(bucket.keys))
	for i, k := range bucket.keys {
		h := hasher.Table(r, k)
		hashes[i] = h
		if seen[h] {
			return false
		}
		if duplicate[h] {
			return false
		}
		duplicate[h] = true
	}

	// Update seen hashes
	for _, h := range hashes {
		seen[h] = true
	}

	// Add the hash index.
	indices[bucket.index] = ri

	// Update the the hash table.
	for i, h := range hashes {
		keys[h] = bucket.keys[i]
		values[h] = bucket.values[i]
	}
	return true
}

func (b *CHDBuilderPoker) Build() (*CHDPoker, error) {
	n := uint64(len(b.keys))
	m := n / 2
	if m == 0 {
		m = 1
	}

	keys := make([]uint32, n)
	//values := make([]uint16, n)
	values := [4888]uint16{}
	hasher := newCHDHasherPoker(n, m)
	buckets := make(bucketVectorPoker, m)
	//indices := make([]uint16, m)
	indices := [2444]uint16{}

	// An extra check to make sure we don't use an invalid index
	for i := range indices {
		indices[i] = ^uint16(0)
	}
	// Have we seen a hash before?
	seen := make(map[uint64]bool)
	// Used to ensure there are no duplicate keys.
	duplicates := make(map[uint32]bool)

	for i := range b.keys {
		key := b.keys[i]
		value := b.values[i]
		if duplicates[key] {
			return nil, fmt.Errorf("duplicate key %d", key)
		}
		duplicates[key] = true
		oh := hasher.HashIndexFromKey(key)

		buckets[oh].index = oh
		buckets[oh].keys = append(buckets[oh].keys, key)
		buckets[oh].values = append(buckets[oh].values, value)
	}

	// Order buckets by size (retaining the hash index)
	collisions := 0
	sort.Sort(buckets)
nextBucket:
	for i, bucket := range buckets {
		if len(bucket.keys) == 0 {
			continue
		}

		// Check existing hash functions.
		for ri, r := range hasher.r {
			if tryHashPoker(hasher, seen, keys, &values, &indices, &bucket, uint16(ri), r) {
				continue nextBucket
			}
		}

		// Keep trying new functions until we get one that does not collide.
		// The number of retries here is very high to allow a very high
		// probability of not getting collisions.
		for i := 0; i < 10000000; i++ {
			if i > collisions {
				collisions = i
			}
			ri, r := hasher.Generate()
			if tryHashPoker(hasher, seen, keys, &values, &indices, &bucket, ri, r) {
				hasher.Add(r)
				continue nextBucket
			}
		}

		// Failed to find a hash function with no collisions.
		return nil, fmt.Errorf("failed to find a collision-free hash function after ~10000000 attempts, for bucket %d/%d with %d entries: %s", i, len(buckets), len(bucket.keys), &bucket)
	}

	// println("max bucket collisions:", collisions)
	// println("keys:", len(table))
	// println("hash functions:", len(hasher.r))

	return &CHDPoker{
		r:       hasher.r,
		r_len:   uint64(len(hasher.r)),
		indices: indices,
		i_len:   uint64(len(indices)),
		values:  values,
		v_len:   uint64(len(values)),
	}, nil
}

func newCHDHasherPoker(size uint64, buckets uint64) *chdHasherPoker {
	rs := rand.NewSource(time.Now().UnixNano())
	c := &chdHasherPoker{size: size, buckets: buckets, rand: rand.New(rs)}
	c.Add(c.random())
	return c
}

func (c *chdHasherPoker) random() uint64 {
	return (uint64(c.rand.Uint32()) << 32) | uint64(c.rand.Uint32())
}

// Hash index from key.
func (h *chdHasherPoker) HashIndexFromKey(b uint32) uint64 {
	return (hasherPoker(b) ^ h.r[0]) % h.buckets
}

// Table hash from random value and key. Generate() returns these random values.
func (h *chdHasherPoker) Table(r uint64, b uint32) uint64 {
	return (hasherPoker(b) ^ h.r[0] ^ r) % h.size
}

func (c *chdHasherPoker) Generate() (uint16, uint64) {
	return c.Len(), c.random()
}

// Add a random value generated by Generate().
func (c *chdHasherPoker) Add(r uint64) {
	c.r = append(c.r, r)
}

func (c *chdHasherPoker) Len() uint16 {
	return uint16(len(c.r))
}

